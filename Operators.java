// Program to show operators that will be used in java
public class Operators {
    public static void main(String[] args) {

        // Arithmetic Operators
        System.out.println("Here are some Arethemetic operators used in java: ");
        System.out.println("Addition:\n1 + 2 = " + (1 + 2));
        System.out.println("Subtraction:\n2 - 1 = " + (2 - 1));
        System.out.println("Multiplication:\n1 * 2 = " + (1 * 2));
        System.out.println("Division:\n4 / 2 = " + (4 / 2));

        // Relational Operators
        System.out.println("\nHere are some Relational operators used in java: ");
        System.out.println("Greater than:\n1 < 2");
        System.out.println("Smaller than:\n2 > 1");
        System.out.println("Greater than or equal to:\n1 <= 2");
        System.out.println("Smaller than or equal to:\n2 >= 1");
        System.out.println("Not equal to:\n 5 != 2");

        // Increment and Decrement Operators
        System.out.println("\nHere are some Increment and Decrement operators used in java: ");
        System.out.println("Increment:\n1++");
        System.out.println("Decrement:\n1--");

        // Logical Operators
        System.out.println("\nHere are some Logical operators used in java: ");
        System.out.println("Logical AND:\ntrue && false");
        System.out.println("Logical OR:\ntrue || false");
        System.out.println("Logical NOT:\n!true");

        // Unary Operators
        System.out.println("\nHere are some Unary operators used in java: ");
        System.out.println("Unary minus:\n-1");
        System.out.println("Unary plus:\n+1");

        // Assignment Operators
        System.out.println("\nHere are some Assignment operators used in java: ");
        System.out.println("Assignment:\nx = 1");
        System.out.println("Assignment:\nx = x + 1");
        System.out.println("Assignment:\nx = x - 1");

        // Bitwise Operator
        System.out.println("\nHere are some Bitwise operators used in java: ");
        System.out.println("Bitwise AND:\n1 & 2");
        System.out.println("Bitwise OR:\n1 | 2");
        System.out.println("Bitwise XOR:\n1 ^ 2");

        // Condional Operator
        System.out.println("\nHere are some Conditional operators used in java: ");
        System.out.println("Conditional:\nif(true) then 1 else 2");
        System.out.println("Conditional:\nif(false) then 1 else 2");
        System.out.println("Conditional:\nif(true) then 1 else if(false) then 2 else 3");
    }
}